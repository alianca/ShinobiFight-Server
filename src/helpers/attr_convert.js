var attribute_base = [ 12, 14, 16, 18, 21, 23, 26, 29, 31, 34, 37, 40, 43, 46, 50, 53, 56, 60, 63, 67, 71, 75, 79, 83, 87, 91, 95, 99, 104, 108, 113, 118, 122, 127, 132, 137, 142, 147, 153, 158, 163, 169, 174, 180, 186, 192, 198, 204, 210, 216 ];
var hp_base = [ 60, 70, 80, 90, 105, 115, 130, 145, 155, 170, 185, 200, 215, 230, 250, 265, 280, 300, 315, 335, 355, 375, 395, 415, 435, 455, 475, 495, 520, 540, 565, 590, 610, 635, 660, 685, 710, 735, 765, 790, 815, 845, 870, 900, 930, 960, 990, 1020, 1050, 1080 ];

exports.convert = function(level, type, reference) {
    var value = (type == 'hp' ? hp_base : attribute_base)[level];

    if (reference[0] == 'm') {
        value += value * 0.25;
    } else if (reference[0] == 'h') {
        value += value * 0.5;
    }

    return Math.round(value);
};
